// ===== User Prompt Template =====
/**
 * Generates the user prompt for product analysis
 * @param {string} userInput - The product description from the user
 * @returns {string} - Formatted user prompt
 */
function generateUserPrompt(userInput) {
    return `请分析以下产品：

${userInput}

请严格按照系统提示中指定的 Markdown 格式输出完整分析。确保：
1. 所有章节完整（产品名称、简介、成本分析、白痴指数计算、评价）
2. 成本表格格式正确
3. 白痴指数计算准确（先写公式，再代入数值，最后得出结果）
4. 分析有深度，不流于表面

开始分析。`;
}

// ===== Chat Follow-up Prompt =====
/**
 * Generates a follow-up chat prompt
 * @param {string} chatMessage - The user's follow-up question
 * @returns {string} - Formatted chat prompt
 */
function generateChatPrompt(chatMessage) {
    return `用户提出了后续问题：

${chatMessage}

请基于之前的分析，回答用户的问题。如果问题涉及成本或白痴指数的调整，请重新计算并说明。`;
}

